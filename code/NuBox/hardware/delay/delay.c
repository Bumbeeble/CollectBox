
#include "delay.h"

/*******************************************************************************
 * 名称: Delay
 * 功能: 简单的延时函数
 * 形参: nCount -> 延时时间数
 * 返回: 无
 * 说明: 无 
 ******************************************************************************/
void Delay(uint32_t nCount)
{
        uint32_t i;
		//nCount *= 16;
        for(i=0;i<nCount;i++);       
}

